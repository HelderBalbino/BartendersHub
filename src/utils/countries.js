const codeToFlag = (code) =>
	code
		.toUpperCase()
		.split('')
		.map((c) => String.fromCodePoint(127397 + c.charCodeAt(0)))
		.join('');

export const countries = rawCountries.map(([code, name]) => ({
	code,
	name,
	flag: codeToFlag(code),
}));

export const findCountry = (codeOrName) => {
	if (!codeOrName) return null;
	const lc = codeOrName.toLowerCase();
	return countries.find(
		(c) => c.code.toLowerCase() === lc || c.name.toLowerCase() === lc,
	);
};

export default countries;
